    public FrmSpp() {
        initComponents();
        sppDao=Factory.getSppDao();
        tblHeader=new String[]{"KODE ANGKATAN", "BIAYA SPP"};
        dtm=new DefaultTableModel(null, tblHeader);
        tblSpp.setModel(dtm);
        
        tblSpp.getSelectionModel().addListSelectionListener(
        new ListSelectionListener(){
            
            public void valueChanged(ListSelectionEvent e){
                baris = tblSpp.getSelectedRow();
                if(baris>=0){
                    txtKodeAngkatan.setText(dtm.getValueAt(baris, 0).toString());
                    txtBiayaSpp.setText(dtm.getValueAt(baris, 1).toString());
                    lblStatus.setText("Record ke : " +(baris+1)+
                            " dari "+dtm.getRowCount());
                }
            }
        });
        refreshTabel();
    }

