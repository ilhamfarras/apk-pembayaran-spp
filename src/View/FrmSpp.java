    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null, 
                "Apakah yakin ingin menghapus data ini?",
                "Konfirmasi", JOptionPane.YES_NO_OPTION)
                ==JOptionPane.YES_OPTION){
            status=sppDao.deleteSpp(txtKodeAngkatan.getText());
            
            if(status = false){
                pesan="Hapus data gagal";
            }
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus",
                    "informasi",JOptionPane.INFORMATION_MESSAGE);
            refreshTabel();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        spp = new EntSpp();
        spp.setKode(txtKodeAngkatan.getText());
        spp.setBiaya(txtBiayaSpp.getText());
        
        if(proses.equalsIgnoreCase("INSERT")){
            status = sppDao.insertSpp(spp);
        }else{
            status = sppDao.updateSpp(spp);
        }
        if(status=false){
            pesan="Penyimpanan data gagal";
        }
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan",
                "informasi",JOptionPane.INFORMATION_MESSAGE);
        refreshTabel();
    }//GEN-LAST:event_btnSaveActionPerformed


